package org.example.leetcode.commons;

import org.example.leetcode.LC0021_MergeTwoSortedLists;

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}